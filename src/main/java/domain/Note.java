
package domain;

public class Note
{
	private Integer id;

	private Integer meetingId;

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public Integer getMeetingId()
	{
		return meetingId;
	}

	public void setMeetingId(Integer meetingId)
	{
		this.meetingId = meetingId;
	}
}
